async function checkApproveAmount (contractAddress, amount)
{
    const contract = GetIERC20(contractAddress);

    await contract.methods.allowance(window.ethereum.selectedAddress, contractAddress).call()
    .then(result => window.unityInstance.SendMessage("Metamask Bridge", "OnCheckApprove", web3.utils.fromWei(result.toString())));
}

async function swapForGothV2 (contractAddress, amount)
{
    const newGothContract = GetContract(contractAddress);
    
    await newGothContract.methods.swapForNewGoth(web3.utils.toWei(amount, 'ether')).send({from: window.ethereum.selectedAddress}, 
      function(err, transactionHash) {
      if (err === null)
      {
        window.unityInstance.SendMessage('Metamask Bridge', 'OnNewGothSwap', "accepted")
      }
      else
      {
        window.unityInstance.SendMessage('Metamask Bridge', 'OnNewGothSwap', "rejected")
      }
      }).then(result => window.unityInstance.SendMessage('Metamask Bridge', 'OnNewGothSwap', result.toString()));
}

async function getBalance (contractAddress, from) {
  const contract = GetIERC20(contractAddress);

  await contract.methods.balanceOf(window.ethereum.selectedAddress).call()
  .then(balance => window.unityInstance
    .SendMessage('Metamask Bridge', 'OnGetBalance', balance.toString() + "," + from + "," + contractAddress));
}

async function approveSpend (contractAddress, amount) {
  const gothContract = GetIERC20(contractAddress);

  console.log('Approve Amount: ', web3.utils.toWei(amount, 'ether'));
  await gothContract.methods.approve(contractAddress, web3.utils.toWei(amount, 'ether')).send({from: window.ethereum.selectedAddress}, 
    function(err, transactionHash) {
      if (err === null)
      {
          window.unityInstance.SendMessage('Metamask Bridge', 'OnApprove', "accepted")
      }
      else
      {
          window.unityInstance.SendMessage('Metamask Bridge', 'OnApprove', "rejected")
      }
    }).then(result => window.unityInstance.SendMessage('Metamask Bridge', 'OnApprove', result.toString()));
}

let currentAccount = null;
function handleAccountsChanged(accounts) {
  if (accounts.length === 0) 
  {
    // MetaMask is locked or the user has not connected any accounts
    console.log('Please connect to MetaMask.');
  } 
  else if (accounts[0] !== currentAccount) 
  {
    currentAccount = accounts[0];
    console.log('Connected With:', window.ethereum.selectedAddress);
    window.unityInstance.SendMessage('Metamask Bridge', 'OnConnected', window.ethereum.selectedAddress);
    // Do any other work!
  }
}


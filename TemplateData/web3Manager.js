function addTokenToWallet (address, symbol, image){
  ethereum
        .request({
          method: 'wallet_watchAsset',
          params: {
            type: 'ERC20',
            options: {
              address: address,
              symbol: symbol,
              decimals: 18,
              image: image,
            },
          },
        })
        .then((success) => {
          if (success) {
            console.log('GOTH successfully added to wallet!');
          } else {
            throw new Error('Something went wrong.');
          }
        })
        .catch(console.error);
}

function connect() {
  ethereum
    .request({ method: 'eth_requestAccounts' })
    .then(handleAccountsChanged)
    .catch((err) => {
      if (err.code === 4001) {
        // EIP-1193 userRejectedRequest error
        // If this happens, the user rejected the connection request.
        console.log('Please connect to MetaMask.');
        window.unityInstance.SendMessage('Metamask Bridge', 'OnConnectCancel');
      } else {
        console.error(err);
      }
    });
}

